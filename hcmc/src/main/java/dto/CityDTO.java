package dto;

public class CityDTO {
	private int city_num;
	private String city_name;
	private String city_address;
	private int city_lati;
	private int city_long;

	public CityDTO() {
	}

	public int getCity_num() {
		return city_num;
	}

	public void setCity_num(int city_num) {
		this.city_num = city_num;
	}

	public String getCity_name() {
		return city_name;
	}

	public void setCity_name(String city_name) {
		this.city_name = city_name;
	}

	public String getCity_address() {
		return city_address;
	}

	public void setCity_address(String city_address) {
		this.city_address = city_address;
	}

	public int getCity_lati() {
		return city_lati;
	}

	public void setCity_lati(int city_lati) {
		this.city_lati = city_lati;
	}

	public int getCity_long() {
		return city_long;
	}

	public void setCity_long(int city_long) {
		this.city_long = city_long;
	}

}// end class