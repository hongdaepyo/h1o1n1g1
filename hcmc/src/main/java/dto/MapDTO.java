package dto;

public class MapDTO {
	private int map_num;
	private int map_number;
	private String map_nume;
	private String map_category;
	private int city_num;

	public MapDTO() {
	}

	public int getMap_num() {
		return map_num;
	}

	public void setMap_num(int map_num) {
		this.map_num = map_num;
	}

	public int getMap_number() {
		return map_number;
	}

	public void setMap_number(int map_number) {
		this.map_number = map_number;
	}

	public String getMap_nume() {
		return map_nume;
	}

	public void setMap_nume(String map_nume) {
		this.map_nume = map_nume;
	}

	public String getMap_category() {
		return map_category;
	}

	public void setMap_category(String map_category) {
		this.map_category = map_category;
	}

	public int getCity_num() {
		return city_num;
	}

	public void setCity_num(int city_num) {
		this.city_num = city_num;
	}

}// end class